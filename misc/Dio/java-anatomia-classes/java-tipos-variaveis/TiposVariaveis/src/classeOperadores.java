import java.util.Date;

public class classeOperadores {

    // classe Operadores.java
    // variável String: valores precisam sempre ser declarados entre aspas "".
    String nome = "ELIEZER";
    // var. int: aceita somente números inteiros
    int idade = 40;
    // var. double ou float: precisa ser digitado ponto "." no lugar de vírgula ",".
    double peso = 78.5;
    // var. char: aceita apenas 1 caráctere, e deve ser atribuído valor entre aspas
    // simples ''.
    char sexo = 'M';
    boolean doadorOrgao = true;
    // criação de objetos: sempre acompanham "new" como atribuição de valor,
    // repete-se o nome do objeto e designa-se um parâmetro "()" para o mesmo.
    Date dataNascimento = new Date();

    // Operadores matemáticos
    // classe Operadores.java
    double soma = 10.5 + 15.7;
    int subtração = 113 - 25;
    int multiplicacao = 20 * 7;
    int divisao = 15 / 3;
    int modulo = 18 % 3;
    double resultado = (10 * 7) + (20 / 4);
}
